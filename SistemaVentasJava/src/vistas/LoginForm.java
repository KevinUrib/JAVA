    public void validar(){
        String dni = txtPassword.getText();
        String user = txtUsuario.getText();
        if (txtUsuario.getText().equals("") || txtPassword.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Debe ingresar datos en las cajas de texto");
            txtPassword.requestFocus();
        } else{
            v = vdao.validarVendedor(dni, user);
            if (v.getUser() != null && v.getDni() != null) {
                Principal p = new Principal();
                p.setVisible(true);
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Debe ingresar usuarios validos");
                txtUsuario.requestFocus();
            }
        }
    }
    
